namespace DataBinding
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MyData Data { set; get; }

        public MainPage()
        {
            this.InitializeComponent();

            Data = new MyData();
            Data.Message = "Hello";
            this.DataContext = Data;
        }

        private void BtnClicked(object sender, RoutedEventArgs e)
        {
            Data.Message = "33333";
        }
    }

    public class MyData : DependencyObject
    {
        public string Message
        {
            get { return (string)GetValue(MessageProperty); }
            set { SetValue(MessageProperty, value); }
        }
        public static readonly DependencyProperty MessageProperty =
            DependencyProperty.Register("Message", typeof(string), typeof(MyData), new PropertyMetadata(0));
    }
}
